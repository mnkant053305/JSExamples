// array initialization techniques
var fruits = [];
fruits = ['Apple', 'Mango', 'Banana'];


var continents = ['Asia', 'Africa', 'Europe', 'North America', 'South America', 'Arctic', 'Antarctica'];

// we should avoid the below initialization for arrays.
var oceans = new Array('Indian Ocean', 'Pacific Ocean', 'Arctic Ocean', 'Antarctic Ocean', 'Atlantic Ocean');


console.log(fruits, continents, oceans);
console.log(fruits instanceof Array);
console.log(continents instanceof Array);
console.log(oceans instanceof Array);


